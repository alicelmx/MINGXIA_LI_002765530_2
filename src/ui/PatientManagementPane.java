    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed

        int selectedRowIndex = tbPatientList.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a patient to view.");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tbPatientList.getModel();
        Patient selectedPatient = (Patient) model.getValueAt(selectedRowIndex, 0);

        PatientProfileFrame viewJFrame = new PatientProfileFrame(selectedPatient);
        viewJFrame.setLocationRelativeTo(null);
        viewJFrame.setVisible(true);
      
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int selectedRowIndex = tbPatientList.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tbPatientList.getModel();
        Patient selectedPatient = (Patient) model.getValueAt(selectedRowIndex, 0);
        
        patientList.deletePatient(selectedPatient);
        // write database
        patientDao.deletePatient(selectedPatient);
        
        // reload        
        populateTable(patientList.getPatientList());
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        
        // exact match 
        String keyword = txtKeyword.getText();
        if(keyword == null || keyword.trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Please Input the Keyword.");
            populateTable(this.patientList.getPatientList());
            return;
        }
        List<Patient> searchResList = this.patientList.searchByKeyword(keyword);
        
        populateTable(searchResList);
    }//GEN-LAST:event_btnSearchActionPerformed

