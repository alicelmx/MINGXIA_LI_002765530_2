    private void populateTable(List<Patient> patientList) {

        DefaultTableModel model = (DefaultTableModel) tbPatientList.getModel();
        model.setRowCount(0);

        for (Patient patient : patientList) {

            Object[] row = new Object[7];
            row[0] = patient;
            row[1] = patient.getFirstName() + " " + patient.getLastName();
            row[2] = patient.getGender();
            row[3] = patient.getPhoneNum();
            row[4] = patient.getEmail();
            row[5] = patient.getCity();
            row[6] = patient.getCommunity();

            model.addRow(row);
        }

    }
