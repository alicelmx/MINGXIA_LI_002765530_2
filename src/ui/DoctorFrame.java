    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tbAppointment.getModel();
        model.setRowCount(0);

        for (AppointmentModel app : appointmentList) {

            Object[] row = new Object[4];
            row[0] = app;
            row[1] = app.getPid();
            Patient patient = patientDao.findPatientInfoByUid(app.getPid());
            row[2] = patient.getFirstName() + " " + patient.getLastName(); // Name;
            row[3] = app.getDatetime();

            model.addRow(row);
        }
    }

